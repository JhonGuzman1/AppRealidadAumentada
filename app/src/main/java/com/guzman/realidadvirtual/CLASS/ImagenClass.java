package com.guzman.realidadvirtual.CLASS;

public class ImagenClass {

    int IdImagen;
    String Nombre;

    public ImagenClass(int idImagen, String nombre) {
        IdImagen = idImagen;
        Nombre = nombre;
    }

    public int getIdImagen() {
        return IdImagen;
    }

    public String getNombre() {
        return Nombre;
    }


}
